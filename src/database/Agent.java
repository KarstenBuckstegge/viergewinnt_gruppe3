package database;

public class Agent {
	private int agent_id;
	private String name;
	
	public int getAgent_id() {
		return agent_id;
	}
	
	public void setAgent_id(int agent_id) {
		this.agent_id = agent_id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}